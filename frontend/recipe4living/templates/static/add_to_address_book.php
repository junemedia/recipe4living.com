              <ul style="margin-bottom: 1.5em;">
                <li>Open the email message and then click on the link directly below the sender's name, titled <b>Add to Address Book</b>.</li>
                <li>Add <em>R4L@recipe4living-recipes.com</em>. The Email Address field should be pre-formatted with <em>R4L@recipe4living-recipes.com</em>.</li>
                <li>Click <b>add</b> when you are finished entering all contact information.</li>
