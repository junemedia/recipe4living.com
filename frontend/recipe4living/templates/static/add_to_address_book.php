            <div style="margin:30px">
              <a name="gmail"></a>

              <h3 style="font-size: 1.2rem; margin-bottom:0.25em; border-bottom: solid 1px;">Gmail<sup>&#153;</sup></h3>
              <ul style="margin-bottom: 1.5em;">
                <li>Click the <b>Contacts</b> link located on the left navigation.</li>
                <li>Click the first icon located under <b>Contacts</b> title.</li>
                <li>Type <em>R4L@recipe4living-recipes.com</em> in the email box located on the far right.</li>
                <li>Click the <b>Save</b> button.</li>
                <li><em>R4L@recipe4living-recipes.com</em> is now saved to your address book. </li>
              </ul>

              <a name="msn"></a>

              <h3 style="font-size: 1.2rem; margin-bottom:0.25em; border-bottom: solid 1px;">MSN/Hotmail<sup>&#174;</sup></h3>
              <ul style="margin-bottom: 1.5em;">
                <li>After opening the message, click the <b>Options</b> button located in the upper right corner.</li>
                <li>Under the 'junk email' section click the <b>Safe and Blocked Senders</b> link.</li>
                <li>Click on the <b>Safe Senders</b> link.</li>
                <li><em>R4L@recipe4living-recipes.com</em> should automatically appear.</li>
                <li>Enter <em>R4L@recipe4living-recipes.com</em> if it does not appear automatically.</li>
                <li>Click the <b>Add to List</b> button.</li>
                <li><em>R4L@recipe4living-recipes.com</em> is now saved to your address book.</li>
              </ul>

              <a name="Yahoo! Mail"></a>

              <h3 style="font-size: 1.2rem; margin-bottom:0.25em; border-bottom: solid 1px;">Yahoo! Mail<sup>&#174;</sup></h3>
              <ul style="margin-bottom: 1.5em;">
                <li>Open the email.</li>
                <li>With your mouse, highlight the email address at the top of the email.</li>
                <li>Right-click the highlighted email address and select <b>Copy</b> from the drop-down menu.</li>
                <li>On the left side of your screen, next to <b>Contacts</b>, click <b>Add</b>.</li>
                <li>In the Email field, right-click and select <b>Paste</b>.</li>
                <li>Click <b>Save</b>.</li>
                <li><em>R4L@recipe4living-recipes.com</em> is now saved in your address book.</li>
              </ul>

              <a name="Yahoo! Mail Classic"></a>

              <h3 style="font-size: 1.2rem; margin-bottom:0.25em; border-bottom: solid 1px;">Yahoo! Mail Classic<sup>&#174;</sup></h3>
              <ul style="margin-bottom: 1.5em;">
                <li>After opening the email, click the <b>Add sender to Contacts</b> icon located to the right of the VerticalResponse <i>from</i> address.</li>
                <li>Click <b>Add to Address Book</b>.</li>
                <li><em>R4L@recipe4living-recipes.com</em> is now saved in your address book.</li>
              </ul>

              <a name="outlook"></a>

              <h3 style="font-size: 1.2rem; margin-bottom:0.25em; border-bottom: solid 1px;">Outlook<sup>&#174;</sup></h3>
              <ul style="margin-bottom: 1.5em;">
                <li>After opening the message, right-click <em>R4L@recipe4living-recipes.com</em>.</li>
                <li>From the drop-down menu, select <b>Add to Outlook contacts...</b></li>
                <li>Click the <b>Save and Close</b> button. </li>
                <li><em>R4L@recipe4living-recipes.com</em> is now saved to your address book. </li>
              </ul>

              <a name="aol"></a>


              <h3 style="font-size: 1.2rem; margin-bottom:0.25em; border-bottom: solid 1px;">AOL 9.0<sup>&#174;</sup></h3>
              <ul style="margin-bottom: 1.5em;">
                <li>Mouse over the Sender or From Name, [R4L@recipe4living-recipes.com]</li>
                <li>In the pop-up window, click the <b>Add Contact</b> link located on the right.</li>
                <li>Add any additional information that you would like in the spaces provided.</li>
                <li>Click <b>Add Contact</b> located at the bottom of the window.</li>
                <li><em>R4L@recipe4living-recipes.com</em> is now saved to your address book </li>
              </ul>


              <a name="comcast"></a>

