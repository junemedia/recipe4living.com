						<p><b>How do I comment on a recipe?</b></p>
						<p>To comment on a recipe, you first need to log in using your username/email address and password. Then, find the recipe you are interested in commenting on. Scroll all the way down to the bottom of the recipe to find a comment box. Add your comment and submit it.</p>
						
